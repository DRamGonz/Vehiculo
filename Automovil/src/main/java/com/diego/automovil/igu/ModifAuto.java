    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        //Todos los datos del vehículo lo guardamos en variales auxiliares. 
        String modelo = txtModelo.getText();
        String marca = txtMarca.getText();
        String motor = txtMotor.getText();
        String color = txtColor.getText();
        String patente = txtPatente.getText();

        int cantPuertas = Integer.parseInt(txtCamPuerta.getText());

        control.modificarAuto(auto, modelo, marca, motor, color, patente, cantPuertas);

        mostrarMensaje("Edición realizada correctamente", "Info", "Edición Exitosa");

        ConsultaVehiculos consul = new ConsultaVehiculos();
        consul.setVisible(true);
        consul.setLocationRelativeTo(null);

        this.dispose();
    }//GEN-LAST:event_btnModificarActionPerformed

    //metodo para enviar mensajes de errores, etc..
    public void mostrarMensaje(String mensaje, String tipo, String titulo) {

        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessage(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);

    }

